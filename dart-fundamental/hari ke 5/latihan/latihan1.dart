void main() {
  print("Looping ke 1");
  var flag = 2;
  while (flag <= 20) {
    print(flag.toString() + " - i love koding");
    // Mengubah nilai flag dengan menambahkan 1
    flag += 2;
  }
  print("Looping ke 2");
  var flag2 = 20;
  while (flag2 >= 2) {
    print(flag2.toString() + " - i will become a mobile developer");
    // Mengubah nilai flag dengan menambahkan 1
    flag2 -= 2;
  }
}
